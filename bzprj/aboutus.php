	<p>
	Launched in January 2014 by Her Excellency Margaret Kenyatta the First Lady of the Republic of Kenya, <br>Beyond Zero was inspired by the realization and knowledge that maternal and children deaths are preventable.<br>
	Beyond Zero is a call to action for policy prioritization and formulation, increased resource allocation, improved service delivery and<br> better individual health seeking behaviors and practices.<br><br>Through this high level advocacy platform, the First Lady leverages on the convening power to bring to focus key challenges facing Kenyans<br> and build strategic partnerships that will address these challenges.
	</p>
	<p style="color:red;" >OUR VISION</p>
	To contribute to the acceleration of economic and social development in Kenya:<br> where there are no preventable deaths of mothers and children; and where there are no social inequities for essential health services.

	<p style="color:red;" >OUR MISSION</p>
